#' hola
#'
#' Say hello to the world.
#'
#' @return String with your greetings.
#' @examples
#' hola()
#' @export
hola <- function() {
  return("Hello, World!")
}

#' suma
#'
#' Add together two numbers.
#'
#' @param x A number.
#' @param y Another number.
#' @return The sum of \code{x} and \code{y}.
#' @examples
#' add(2, 3)
#' @export
suma <- function(x, y) {
  x + y
}
